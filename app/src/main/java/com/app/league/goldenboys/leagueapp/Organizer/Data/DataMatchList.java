package com.app.league.goldenboys.leagueapp.Organizer.Data;

public class DataMatchList {

    private String matchName;
    private String matchOpponentA;
    private String matchOpponentB;
    private String matchSchedule;
    private String matchOpponentAScore;
    private String matchOpponentBScore;
    private String matchPlayerOfTheGame;
    private String matchWinner;

    public DataMatchList() {
    }

    public String getMatchName() {
        return matchName;
    }

    public void setMatchName(String matchName) {
        this.matchName = matchName;
    }

    public String getMatchOpponentA() {
        return matchOpponentA;
    }

    public void setMatchOpponentA(String matchOpponentA) {
        this.matchOpponentA = matchOpponentA;
    }

    public String getMatchOpponentB() {
        return matchOpponentB;
    }

    public void setMatchOpponentB(String matchOpponentB) {
        this.matchOpponentB = matchOpponentB;
    }

    public String getMatchSchedule() {
        return matchSchedule;
    }

    public void setMatchSchedule(String matchSchedule) {
        this.matchSchedule = matchSchedule;
    }

    public String getMatchOpponentAScore() {
        return matchOpponentAScore;
    }

    public void setMatchOpponentAScore(String matchOpponentAScore) {
        this.matchOpponentAScore = matchOpponentAScore;
    }

    public String getMatchOpponentBScore() {
        return matchOpponentBScore;
    }

    public void setMatchOpponentBScore(String matchOpponentBScore) {
        this.matchOpponentBScore = matchOpponentBScore;
    }

    public String getMatchPlayerOfTheGame() {
        return matchPlayerOfTheGame;
    }

    public void setMatchPlayerOfTheGame(String matchPlayerOfTheGame) {
        this.matchPlayerOfTheGame = matchPlayerOfTheGame;
    }

    public String getMatchWinner() {
        return matchWinner;
    }

    public void setMatchWinner(String matchWinner) {
        this.matchWinner = matchWinner;
    }
}
